# Program that can take any integer input from the user
# and calculate the power and square root of that number.

# Taking a number from the user
num = int(input("Enter a number: "))

# Formula to calculate the power of the input number
pow = num ** 2
# Print the result
print("Power of",num,"is",pow)

# Formula to calculate the square root of the input number
sqr = num ** 0.5
# Print the result
print("Square root of",num,"is",sqr)